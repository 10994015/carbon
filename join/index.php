       <h4>會員資格</h4>
       <p>本會會員分為下列五種，入會資格：凡贊同本會宗旨且年滿二十歲(學生會員除外)，具以下資格者，得申請成為本會會員。</p>
       <ol style="list-style-type:cjk-ideographic;">
           <li>學生會員：<br>
凡大學、獨立學院或專科院校相關科系在學學生，向本會申請，經理事會審查通過，得為學生會員。</li>
           <li>普通會員：<br>
具有下列資格之一者，向本會申請，經理事會審查通過，皆得為普通會員。<br>
1.大學、獨立學院或專科院校相關科系畢業，並從事碳材料相關領域工作者。<br>
2.學生會員畢業後，得申請轉為普通會員。<br>
3.對碳材料之研究與應用有興趣者。</li>
<li>
    永久會員：<br>
凡具有普通會員之資格，並一次繳足永久會費者。
</li>
<li>
    團體會員：<br>
凡認同本會宗旨之機構或團體，向本會申請， 經理事會審查通過， 並繳納會費後， 得為團體會員。 團體會員推
派代表一人，以行使其會員權利。
</li>
<li>
    榮譽會員：<br>
凡國內外人士具下列資格之一， 由理事長或三名以上會員推薦， 經理事會通過後， 得聘為榮譽會員。 榮譽會員
得免繳入會費及常年會費。
1.對碳材料相關領域之學術或產業發展有特殊貢獻者。
2.對本會會務有特殊貢獻者。
</li>
       </ol >
